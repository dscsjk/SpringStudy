package prj0626;

public class OrdList {
	// 2.선택 주문메뉴에 관련된 구조체
	private String mCd;
	private String mName;
	private int mPrice;

	public String getmCd() {
		return mCd;
	}
	public void setmCd(String mCd) {
		this.mCd = mCd;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public int getmPrice() {
		return mPrice;
	}
	public void setmPrice(int mPrice) {
		this.mPrice = mPrice;
	}
}
